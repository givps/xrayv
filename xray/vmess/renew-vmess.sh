read -rp "Input Username : " user
if [ -z $user ]; then
m-vmess
else
read -p "Expired (days): " expired
exp=$(grep -wE "^#### $user" "/usr/local/etc/xray/config.json" | cut -d ' ' -f 3 | sort | uniq)
